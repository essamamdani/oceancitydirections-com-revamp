-- businesses table with all indexes and constraints
CREATE TABLE public.businesses (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  title text NULL,
  description text NULL,
  category_id integer NULL,
  subcategories_id integer[] NULL,
  cid text NULL,
  feature_id text NULL,
  address text NULL,
  address_info jsonb NULL,
  place_id text NULL,
  phone text NULL,
  url text NULL,
  domain text NULL,
  logo text NULL,
  main_image text NULL,
  total_photos integer NULL,
  snippet text NULL,
  latitude double precision NULL,
  longitude double precision NULL,
  is_claimed boolean NULL,
  attributes jsonb NULL,
  place_topics jsonb NULL,
  rating jsonb NULL,
  rating_distribution jsonb NULL,
  people_also_search jsonb NULL,
  work_time jsonb NULL,
  popular_times jsonb NULL,
  local_business_links jsonb NULL,
  contacts jsonb NULL,
  time_update timestamp with time zone NULL,
  check_url text NULL,
  price_level text NULL,
  hotel_rating integer NULL,
  slug text NULL,
  state text NULL,
  city text NULL,
  zip text NULL,
  county text NULL,
  rating_value numeric GENERATED ALWAYS AS (
    COALESCE(
      NULLIF(
        ((rating ->> 'value'::text))::numeric,
        null::numeric
      ),
      (0)::numeric
    )
  ) STORED NULL,
  state_lower text GENERATED ALWAYS AS (lower(state)) STORED NULL,
  city_lower text GENERATED ALWAYS AS (lower(city)) STORED NULL,
  county_lower text GENERATED ALWAYS AS (lower(county)) STORED NULL,
  slug_tsv tsvector GENERATED ALWAYS AS (to_tsvector('english'::regconfig, slug)) STORED NULL,
  geo_point geometry(Point,4326) GENERATED ALWAYS AS (
    st_setsrid(st_makepoint(longitude, latitude), 4326)
  ) STORED NULL,
  status boolean NOT NULL DEFAULT true,
  update_slug text NULL,
  claimed_by uuid NULL,
  claimed_approval boolean NULL DEFAULT false,
  remove_request uuid NULL,
  deleted_at timestamp with time zone NULL,
  CONSTRAINT businesses__pkey PRIMARY KEY (id),
  CONSTRAINT businesses__slug_key UNIQUE (slug),
  CONSTRAINT businesses__category_id_fkey FOREIGN KEY (category_id) REFERENCES categories (id)
)
WITH (
  parallel_workers = 4,
  autovacuum_vacuum_scale_factor = 0.01,
  autovacuum_analyze_scale_factor = 0.005
) TABLESPACE pg_default;

