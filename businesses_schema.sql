-- categories table
CREATE TABLE public.categories (
  id serial NOT NULL,
  name text NOT NULL,
  featured boolean NOT NULL DEFAULT false,
  CONSTRAINT categories_pkey PRIMARY KEY (id),
  CONSTRAINT categories_name_key UNIQUE (name)
) TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS categories_featured_idx ON public.categories USING btree (featured) TABLESPACE pg_default;
CREATE INDEX IF NOT EXISTS idx_categories_id ON public.categories USING btree (id) TABLESPACE pg_default;
CREATE INDEX IF NOT EXISTS idx_categories_name ON public.categories USING btree (name) TABLESPACE pg_default;
CREATE INDEX IF NOT EXISTS idx_categories_name_lower ON public.categories USING btree (lower(name)) TABLESPACE pg_default;
CREATE INDEX IF NOT EXISTS idx_categories_name_trgm ON public.categories USING gin (name gin_trgm_ops) TABLESPACE pg_default;

-- subcategories table
CREATE TABLE public.subcategories (
  id serial NOT NULL,
  category_id integer NULL,
  name text NOT NULL,
  cat_id_sub_name text NULL,
  CONSTRAINT subcategories_pkey PRIMARY KEY (id),
  CONSTRAINT subcategories_cat_id_sub_name_key UNIQUE (cat_id_sub_name),
  CONSTRAINT subcategories_category_id_fkey FOREIGN KEY (category_id) REFERENCES categories (id) ON DELETE CASCADE
